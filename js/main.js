function calcPixel(unixTime)
{
var base = unixMax - unixMin;
var offset = unixTime - unixMin;
var percent = offset / base;
var pixel = Math.floor(percent*canvasWidth);
if (pixel<1){pixel=1;}
return pixel;
}


